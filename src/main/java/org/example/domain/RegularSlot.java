package org.example.domain;

public class RegularSlot {

    private int firstShot;

    private int secondShot;

    private int positionSlot;

    private int result;


    public int getFirstShot() {
        return firstShot;
    }

    public void setFirstShot(int firstShot) {
        this.firstShot = firstShot;
    }

    public int getSecondShot() {
        return secondShot;
    }

    public void setSecondShot(int secondShot) {
        this.secondShot = secondShot;
    }

    public int getPositionSlot() {
        return positionSlot;
    }

    public void setPositionSlot(int positionSlot) {
        this.positionSlot = positionSlot;
    }

    public int getResult() {
        return result;
    }

    public void setResult(int result) {
        this.result = result;
    }
}
